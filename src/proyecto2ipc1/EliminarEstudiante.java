    private void bobtenerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bobtenerActionPerformed
        // TODO add your handling code here:
        NodoEstudiante aux;
        aux = Proyecto2IPC1.estudiante.primero;
        do {
            if(jComboBox1.getSelectedItem()==aux.carnet){
                txtdpi.setText(aux.dpi);
                //txtapellido.setText(aux.Apellido);
                txtnombre.setText(aux.Nombre);
                txtdireccion.setText(aux.direccion);
                txtcredito.setText(aux.credito);
                txtcontra.setText(aux.contrasena);
                txtcorreo.setText(aux.correo);
            }
            aux = aux.siguiente;
        } while (aux != Proyecto2IPC1.estudiante.primero);
    }//GEN-LAST:event_bobtenerActionPerformed

    private void beliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beliminarActionPerformed
        // TODO add your handling code here:
        NodoEstudiante posicion_actual;
        NodoEstudiante anterior;
        posicion_actual=Proyecto2IPC1.estudiante.primero;
        anterior=Proyecto2IPC1.estudiante.ultimo;
        
        do {
            if(jComboBox1.getSelectedItem()==posicion_actual.carnet){
                if(posicion_actual==Proyecto2IPC1.estudiante.primero){
                    Proyecto2IPC1.estudiante.primero=Proyecto2IPC1.estudiante.primero.siguiente;
                    Proyecto2IPC1.estudiante.ultimo.siguiente=Proyecto2IPC1.estudiante.primero;
                    Proyecto2IPC1.estudiante.primero.anterior=Proyecto2IPC1.estudiante.ultimo;
                    JOptionPane.showMessageDialog(null, "Se ha realizado Eliminado la primera posicion", "Eliminar", 1);
                }else if(posicion_actual==Proyecto2IPC1.estudiante.ultimo){
                     Proyecto2IPC1.estudiante.ultimo= Proyecto2IPC1.estudiante.ultimo.anterior;
                     Proyecto2IPC1.estudiante.primero.anterior= Proyecto2IPC1.estudiante.ultimo;
                     Proyecto2IPC1.estudiante.ultimo.siguiente= Proyecto2IPC1.estudiante.primero;  
                     JOptionPane.showMessageDialog(null, "Se ha Eliminado la ultima posicion", "Eliminar", 1);
                      
                    
                }else{
                     anterior.siguiente=posicion_actual.siguiente;
                     posicion_actual.siguiente.anterior=anterior;
                     JOptionPane.showMessageDialog(null, "Se ha eliminado el usuario", "Eliminar", 1);
                }
                /*txtdpi.setText(aux.dpi);
                txtapellido.setText(aux.Apellido);
                txtnombre.setText(aux.Nombre);
                txtdireccion.setText(aux.direccion);
                txtcredito.setText(aux.credito);
                txtcontra.setText(aux.contrasena);
                txtcorreo.setText(aux.correo);*/
            }
            anterior=posicion_actual;
            posicion_actual = posicion_actual.siguiente;
        } while (posicion_actual != Proyecto2IPC1.estudiante.primero);
    }//GEN-LAST:event_beliminarActionPerformed

